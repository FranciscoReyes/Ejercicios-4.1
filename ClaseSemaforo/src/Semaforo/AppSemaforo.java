    private void botonSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSubirActionPerformed
        semaf.subir();
        
        
        switch (semaf.getColor()) {
            case 1:
                campoRojo.setBackground(Color.red);
                campoAmbar.setBackground(Color.WHITE);
                campoVerde.setBackground(Color.WHITE);
                break;
            case 2:
                campoRojo.setBackground(Color.WHITE);
                campoAmbar.setBackground(Color.ORANGE);
                campoVerde.setBackground(Color.WHITE);
                break;
            case 3:
                campoRojo.setBackground(Color.WHITE);
                campoAmbar.setBackground(Color.WHITE);
                campoVerde.setBackground(Color.GREEN);
                break;
        }
        
        
        if (semaf.isParpadeando() == true) {
            indicadorParpadeo.setText("Parpadeando");
        } else {
            indicadorParpadeo.setText("");
        }
    }//GEN-LAST:event_botonSubirActionPerformed

